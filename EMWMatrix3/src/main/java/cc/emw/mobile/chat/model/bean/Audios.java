package cc.emw.mobile.chat.model.bean;

public class Audios {

	private String Url;// 音频路径
	public String Length;// 音频大小

	public String getUrl() {
		return Url;
	}

	public void setUrl(String url) {
		Url = url;
	}

	public String getLength() {
		return Length;
	}

	public void setLength(String length) {
		Length = length;
	}
}
