    private String iconCode;
    private Context mContext;
    private static Map<String, String> iconMap;

    public IconTextView(Context context) {
        this(context, null);
    }

    public IconTextView(Context context, AttributeSet attrs) {
        this(context, attrs, 0);
    }

    public IconTextView(Context context, AttributeSet attrs, int defStyle) {
        super(context, attrs, defStyle);
        this.mContext = context;
        TypedArray array = context.getTheme().obtainStyledAttributes(
                attrs, R.styleable.IconTextView, defStyle, 0);
        int n = array.getIndexCount();
        for (int i = 0; i < n; i++) {
            int attr = array.getIndex(i);
            switch (attr) {
                case R.styleable.IconTextView_iconCode:
                    iconCode = array.getString(attr);
                    setIconText(context, this, iconCode);
                default:
                    break;
            }
        }
        array.recycle();
    }

    public void setIconText(String iconCode) {
        setIconText(mContext, this, iconCode);
    }

    /**
     * 设置字体图标
     *
     * @param context
     * @param textView
     * @param fontCode 图标代码
     */
    public static void setIconText(Context context, TextView textView, String fontCode) {
        StringBuilder str = new StringBuilder();
        if (!TextUtils.isEmpty(fontCode)) {
            str.append("&#x").append(fontCode).append(";");
        } else {
            str.append("&#xe931;");
        }
        //加载字体文件
        Typeface typeface = Typeface.createFromAsset(context.getAssets(), "iconfont/EMW-matrix.ttf");
        textView.setTypeface(typeface);
        textView.setText(Html.fromHtml(str.toString())); // 如 "&#xe931;"
    }

    /**
     * 通过字体图标名称获取图标代码
     * @param icon 图标名称
     * @return
     */
    public static String getIconCode(String icon){
        if (!TextUtils.isEmpty(icon) && iconMap != null && iconMap.get(icon.split(" ")[0]) != null) {
            return iconMap.get(icon.split(" ")[0]);
        } else{
            return "";
        }
    }

